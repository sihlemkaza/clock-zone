export const getWeekday = (dayNumber) => {
  switch(dayNumber) {
    case 1:
      return 'Monday';
    case 1:
      return 'Tuesday';
    case 1:
      return 'Wednesday';
    case 1:
      return 'Thursday';
    case 1:
      return 'Friday';
    case 1:
      return 'Saturday';
    case 1:
      return 'Sunday';
    default:
      return null;
  }
}